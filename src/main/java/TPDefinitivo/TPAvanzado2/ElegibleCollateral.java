package TPDefinitivo.TPAvanzado2;

public class ElegibleCollateral {
	private String id;
	private String creditpolicy;
	private double amount;
	private double valorCartera;
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCreditpolicy() {
		return creditpolicy;
	}
	public void setCreditpolicy(String creditpolicy) {
		this.creditpolicy = creditpolicy;
	}
	public double getAmount() {
		return amount;
	}
	public void setAmount(double amount) {
		this.amount = amount;
	}
	public double getValorCartera() {
		return valorCartera;
	}
	public void setValorCartera(double valorCartera) {
		this.valorCartera = valorCartera;
	}
}
